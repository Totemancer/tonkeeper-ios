import Foundation
import KeeperCore

enum BackgroundUpdateState {
  case connecting
  case connected
  case disconnected
  case noConnection
  
  init(state: BackgroundUpdateStore.State) {
    switch state {
    case .connecting:
      self = .connecting
    case .connected:
      self = .connected
    case .disconnected:
      self = .disconnected
    case .noConnection:
      self = .noConnection
    }
  }
}
